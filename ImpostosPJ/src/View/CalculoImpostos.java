    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        // Capturando o valor da nota digitado pelo usuÃ¡rio.
        double nf = Double.parseDouble(jTextFieldValorNotaFiscal.getText());

        //Calculando os impostos e mostrando os cÃ¡lculos na tela 
        NumberFormat formatoMoeda = NumberFormat.getCurrencyInstance();
        jTextFieldISS.setText(formatoMoeda.format((nf * 0.02)));
        jTextFieldCONFINS.setText((nf > 5000) ? formatoMoeda.format((nf * 0.03)) : "ISENTO");
        jTextFieldPIS.setText((nf > 5000) ? formatoMoeda.format((nf * 0.0065)) : "ISENTO");
        jTextFieldCSLL.setText(formatoMoeda.format((nf * 0.0188)));
        jTextFieldCPRP.setText(formatoMoeda.format((nf * 0.025)));
    }//GEN-LAST:event_jButtonCalcularActionPerformed

